let noGrammarCount = 0;

words.forEach(w => {
  const catKey = w.hanzi + '|' + w.category[0];
  const hanziKey = w.hanzi;

  // mnemonic は上書きしない。文法データがある単語にのみ grammar フィールドを追加
  if (grammarMnemonics[catKey]) {
    w.grammar = grammarMnemonics[catKey];
    grammarCount++;
  } else if (grammarMnemonics[hanziKey]) {
    w.grammar = grammarMnemonics[hanziKey];
    grammarCount++;
  } else {
    // 文法ポイントがない単語には grammar フィールドを追加しない
    noGrammarCount++;
  }
});

